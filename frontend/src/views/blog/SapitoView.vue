      <li><strong>Fuente de alimento:</strong> Actúa como presa para numerosos animales superiores, incluyendo aves acuáticas, rapaces nocturnas, reptiles y pequeños mamíferos, ayudando así a mantener el equilibrio en el ecosistema.</li>
