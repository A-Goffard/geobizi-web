        <h2>¿Qué se entiende por Patrimonio Cultural Inmaterial (PCI)?</h2>
        <p>La UNESCO define el PCI, destacando su papel fundamental en prácticas, usos, expresiones, saberes y técnicas que son transmitidos de una generación a otra. Este legado intangible no solo otorga a las comunidades un arraigado sentido de identidad, sino que también promueve la creatividad y el bienestar social. Más allá de su dimensión cultural, el patrimonio inmaterial desempeña un papel crucial en la gestión tanto del entorno natural como del entorno social.</p>
        <p>La perspectiva de la UNESCO sobre el PCI abarca usos, representaciones, expresiones, conocimientos y técnicas, incluyendo instrumentos, objetos y espacios culturales que las comunidades, grupos e incluso individuos reconocen como elementos esenciales de su patrimonio cultural. Este rico legado no solo se transmite a lo largo de las generaciones, sino que también se recrea continuamente, adaptándose a los cambios en el entorno, la interacción con la naturaleza y la historia de las comunidades. Este proceso dinámico confiere a estas comunidades un profundo sentido de identidad y continuidad, contribuyendo significativamente a fomentar el respeto por la diversidad cultural y nutrir la creatividad humana.</p>

        <h3>Características del Patrimonio Cultural Inmaterial</h3>
        <ul>
            <li>Naturaleza compartida por los miembros de una comunidad.</li>
            <li>Transmisión y recreación principalmente oral.</li>
            <li>Tendencia a ser transmitido desde la infancia.</li>
            <li>Integración en nuestra memoria colectiva.</li>
            <li>Intrincada relación con la dimensión material de la cultura.</li>
            <li>Contextualizado en un tiempo y espacio específicos.</li>
            <li>Conexión con las formas de vida y la cotidianidad.</li>
            <li>Naturaleza ritualizada.</li>
            <li>La vivencia sensorial que proporciona, siendo apreciado a través del olfato, el gusto, la percepción y la comprensión sensorial.</li>
            <li>Carácter dinámico y vital.</li>
            <li>Vulnerabilidad, siendo susceptible incluso de perderse.</li>
        </ul>

        <h2>¿Cuáles son los ámbitos en los que se manifiesta el PCI?</h2>
        <p>El PCI no se limita a compartimentos estancos o limitados; por el contrario, diversos elementos culturales pueden manifestarse en distintos ámbitos y estar interrelacionados, como el baile, la música y las festividades.</p>
        <p>Este PCI siempre guarda una estrecha conexión con el patrimonio material, que se manifiesta en herramientas, instrumentos musicales, indumentarias, objetos, comidas y lugares, entre otros. Como resultado, el PCI toma forma concreta en espacios y objetos tangibles.</p>

        <h3>Tradiciones y expresiones orales</h3>
        <p>La lengua es esencial en el Patrimonio Inmaterial, siendo la herramienta clave para la transmisión cultural oral. Los dialectos y formas habladas, como el hika en euskera, son parte del PCI. La toponimia y la literatura oral, que incluye refranes, cuentos y leyendas, también forman parte del Patrimonio Inmaterial de Bizkaia.</p>

        <h3>Artes del espectáculo</h3>
        <p>Dentro del PCI en este ámbito, destacan la música vocal e instrumental en eventos como romerías, pasacalles y tamborradas. Danzas tradicionales como Dantzari-dantza y Ezpatadantza, así como teatro popular como el Entierro del Pellejo en Muxika, forman parte del PCI.</p>

        <h3>Usos sociales, rituales y actos festivos</h3>
        <p>El PCI abarca rituales festivos ligados a distintas etapas de la vida, como nacimientos, bodas y muerte, incluyendo creencias sobre la defunción y el duelo. Las festividades anuales, como Navidad, Carnaval, San Juan y Todos los Santos, incorporan en cada localidad rituales, creencias, canciones, bailes, juegos, indumentaria y comidas tradicionales.</p>

        <h3>Técnicas artesanales tradicionales</h3>
        <p>En este ámbito se abordan las técnicas y materias primas de la artesanía tradicional, centrándose más en los conocimientos, procesos, saberes y materiales que se emplean. Incluye herramientas, prendas, joyas, indumentaria, accesorios, recipientes, instrumentos musicales, enseres domésticos, juguetes, muebles característicos de la artesanía local.</p>

        <h3>Conocimientos y usos relacionados con la Naturaleza y el Universo</h3>
        <p>Este ámbito abarca creencias diversas sobre la fauna, flora, meteorología, ciclos lunares, témporas y cambios estacionales. Incluye también la medicina tradicional con sus remedios, el uso de plantas medicinales, la figura de los curanderos y las creencias acerca de las causas o curas de algunas enfermedades.</p>

