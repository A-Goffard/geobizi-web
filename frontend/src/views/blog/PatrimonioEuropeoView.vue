    <h2>¿Qué se entiende por Patrimonio Cultural Inmaterial (PCI)?</h2>
    <p>La <strong>UNESCO</strong> define el Patrimonio Cultural Inmaterial (PCI) destacando su papel fundamental en prácticas, usos, expresiones, saberes y técnicas que son transmitidos de una generación a otra. Este legado intangible no solo otorga a las comunidades un arraigado sentido de identidad, sino que también promueve la creatividad y el bienestar social.</p>
    <p>Este rico legado se recrea continuamente, adaptándose a los cambios del entorno y a la interacción con la naturaleza. Este proceso dinámico confiere a las comunidades un profundo sentido de continuidad, contribuyendo a fomentar el respeto por la diversidad cultural.</p>

    <h3>Características del Patrimonio Cultural Inmaterial</h3>
    <ul>
      <li><strong>Comunitario:</strong> Naturaleza compartida por los miembros de una comunidad.</li>
      <li><strong>Transmisión oral:</strong> Se recrea y transmite principalmente de forma oral, a menudo desde la infancia.</li>
      <li><strong>Identitario:</strong> Integración absoluta en nuestra memoria colectiva.</li>
      <li><strong>Interconectado:</strong> Intrincada relación con la dimensión material de la cultura (lugares, herramientas).</li>
      <li><strong>Dinámico y Vital:</strong> No es un elemento fosilizado; está vivo, es ritualizado y cambia con el tiempo.</li>
      <li><strong>Sensorial:</strong> Proporciona una vivencia que se aprecia a través del olfato, el gusto y la percepción.</li>
      <li><strong>Vulnerable:</strong> Es susceptible de perderse si no se protege y se practica.</li>
    </ul>

    <h2>Ámbitos de manifestación del PCI</h2>
    <p>El PCI no se limita a compartimentos estancos; guarda una estrecha conexión con el patrimonio material (herramientas, instrumentos, indumentarias, lugares). Se manifiesta principalmente en:</p>

    <ul>
      <li><strong>Tradiciones y expresiones orales:</strong> La lengua como vehículo (ej. el dialecto <em>hika</em> en euskera), toponimia, refranes, cuentos y leyendas.</li>
      <li><strong>Artes del espectáculo:</strong> Música vocal e instrumental (romerías, pasacalles) y danzas tradicionales (<em>Dantzari-dantza</em>, <em>Ezpatadantza</em>).</li>
      <li><strong>Usos sociales, rituales y festivos:</strong> Eventos ligados al ciclo de la vida y festividades anuales (Carnaval, San Juan, Todos los Santos).</li>
      <li><strong>Técnicas artesanales tradicionales:</strong> Saberes y procesos empleados para crear herramientas, indumentaria, instrumentos musicales o enseres.</li>
      <li><strong>Conocimientos relacionados con la Naturaleza:</strong> Creencias sobre la fauna, flora, meteorología, ciclos lunares y la medicina tradicional (uso de plantas medicinales).</li>
    </ul>

